app.put(
  "/api/order-status/:orderId",
  requireSignIn,
  isAdmin,
  async (req, res) => {
    try {
      const { orderId } = req.params;
      const { status } = req.body;
      const orders = await orderModel.findByIdAndUpdate(
        orderId,
        { status },
        { new: true }
      );
      res.json(orders);
    } catch (error) {
      console.log(error);
      res.status(500).send({
        success: false,
        message: "Error While Updateing Order",
        error,
      });
    }
  }
);

//run listen
app.listen(PORT, () => {
  console.log(`Server Running on port ${PORT}`);
});
